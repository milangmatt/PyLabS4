len_rect = int(input("Enter Length of rectangle: "))
bre_rect = int(input("Enter Breadth of rectangle: "))
rect_area = len_rect * bre_rect
bas_tri = int(input("Enter Base of triangle: "))
hei_tri = int(input("Enter Height of triangle: "))
tri_area = 0.5 * bas_tri * hei_tri
print (f" Area of the Shapes are: \n Rectangle : {rect_area} \n Triangle : {tri_area}")
